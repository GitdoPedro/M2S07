CREATE TABLE PROFESSORES (
     ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
     NOME VARCHAR2(64) NOT NULL,
     SOBRENOME VARCHAR2(64) NOT NULL,
     MATRICULA VARCHAR(14) NOT NULL,
     EMAIL VARCHAR(48) NOT NULL,
     DATANASCIMENTO VARCHAR(48) NOT NULL,
     NACIONALIDADE VARCHAR(24) NOT NULL,
     PRIMARY KEY(ID)
     );
     
CREATE TABLE ESTUDANTES (
     ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
     NOME VARCHAR2(64) NOT NULL,
     SOBRENOME VARCHAR2(64) NOT NULL,
     MATRICULA VARCHAR(14) NOT NULL,
     EMAIL VARCHAR(48) NOT NULL,
     DATANASCIMENTO VARCHAR(48) NOT NULL,
     NACIONALIDADE VARCHAR(24) NOT NULL,
     MEDIAGERAL FLOAT(12) NULL,
     PRIMARY KEY(ID)
     );
     

CREATE TABLE CURSOS(
        ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
        NOME VARCHAR2(64) NOT NULL,
        MODALIDADE VARCHAR2(16) NOT NULL,
        ID_PROFESSORES NUMBER NOT NULL,
        QUANTIDADEALUNOS CHAR(20) DEFAULT '0',
        PRIMARY KEY(ID),
        FOREIGN KEY(ID_PROFESSORES) REFERENCES PROFESSORES(ID)
    )